// 8. Sorting an Array
// Create an array of names containing at least 4 names.
// Use the sort method to sort the names alphabetically.
// Display the sorted array.

// Create an array of names containing at least 4 names
const names = ['Zara', 'Michael', 'Ali', 'Sophia'];

// Use the sort method to sort the names alphabetically
const sortedNames = names.sort();

// Display the sorted array
console.log('Sorted Names:', sortedNames);
// Output: ['Ali', 'Michael', 'Sophia', 'Zara']
